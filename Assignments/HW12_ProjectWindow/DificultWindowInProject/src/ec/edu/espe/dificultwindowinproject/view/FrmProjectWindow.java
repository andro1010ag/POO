    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        int code;
        int quantity;
        String description;
        float price;
        
        
        if (!txtCode.getText().isEmpty()) {
            code = Integer.parseInt(txtCode.getText());
            if (!txtQuantity.getText().isEmpty()) {
                quantity = Integer.parseInt(txtQuantity.getText());
                if (!txtDescription.getText().isEmpty()) {
                    description = txtDescription.getText();
                    if (!txtPrice.getText().isEmpty()) {
                        price = Float.parseFloat(txtPrice.getText());
                        addProduct(code, quantity, description, price);
                    } else {
                        JOptionPane.showMessageDialog(this, "Ingrese el Precio del producto");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Ingrese la Descripcion del producto");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese la cantidad del producto");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese el codigo del producto");
        }
    }//GEN-LAST:event_BtnAddActionPerformed

    private void txtQuantityKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQuantityKeyTyped
        char validate=evt.getKeyChar();
        
        if (Character.isLetter(validate)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "The input is not valid");
        }
    }//GEN-LAST:event_txtQuantityKeyTyped


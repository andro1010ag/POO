    private void BtnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoveActionPerformed
        DefaultTableModel tm = (DefaultTableModel) TblProduct.getModel();
        if(TblProduct.getSelectedRowCount()>0){
        tm.removeRow(TblProduct.getSelectedRow());
        
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione por lo menos un producto");
        }
    }//GEN-LAST:event_BtnRemoveActionPerformed

    private void BtnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditActionPerformed
        if(selectedRow!=-1){
            TblProduct.setValueAt(txtCode.getText(), selectedRow, 0);
            TblProduct.setValueAt(txtQuantity.getText(), selectedRow, 1);
            TblProduct.setValueAt(txtDescription.getText(), selectedRow, 2);
            TblProduct.setValueAt(txtPrice.getText(), selectedRow, 3);
            clean();
            selectedRow = -1;
        }else{
            JOptionPane.showMessageDialog(this, "No se seleccionó ningún dato");
        
        }
        
    }//GEN-LAST:event_BtnEditActionPerformed

    private void BtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSearchActionPerformed
        try{
            selectedRow = TblProduct.getSelectedRow();
            txtCode.setText(TblProduct.getValueAt(TblProduct.getSelectedRow(), 0).toString());
            txtQuantity.setText(TblProduct.getValueAt(TblProduct.getSelectedRow(), 1).toString());
            txtDescription.setText(TblProduct.getValueAt(TblProduct.getSelectedRow(), 2).toString());
            txtPrice.setText(TblProduct.getValueAt(TblProduct.getSelectedRow(), 3).toString());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        
        }
        
    }//GEN-LAST:event_BtnSearchActionPerformed
    
    private void addProduct(int code, int quantity, String description, float price){
        try{
        DefaultTableModel tm = (DefaultTableModel) TblProduct.getModel();
        Object newRow[] = {code,quantity,description,price};
        tm.addRow(newRow);
        JOptionPane.showMessageDialog(this, "Producto añadido con exito");
        clean();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        
        }
    }
    private void clean(){
        txtCode.setText("");
        txtQuantity.setText("");
        txtDescription.setText("");
        txtPrice.setText("");
    
    }
